int	ft_atoi(char *str)
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	str = str + i;
	i = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
		if (*str++ == '-')
			k++;
	while (*str >= '0' && *str <= '9')
	{
		i = i * 10 + *str - '0';
		str++;
	}
	if ((k > 0))
		i = i * -1;
	return (i);
}
